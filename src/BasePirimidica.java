public class BasePirimidica {
    private String citosina;
    private String timina;

    public BasePirimidica(String citosina, String timina){
        this.citosina = citosina;
        this.timina = timina;
    }

    public String getCitosina(){
        return this.citosina;
    }

    public String getTimina(){
        return this.timina;
    }
}
