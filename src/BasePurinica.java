public class BasePurinica {
    private String adenina;
    private String guanina;

    public BasePurinica(String adenina, String guanina){
        this.adenina = adenina;
        this.guanina = guanina;
    }

    public String getAdenina(){
        return this.adenina;
    }

    public String getGuanina(){
        return this.guanina;
    }
}

